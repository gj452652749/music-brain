package org.gj.sound;

public class SoundBean {
	int pitch;
	int grey;
	
	public SoundBean(int pitch, int grey) {
		super();
		this.pitch = pitch;
		this.grey = grey;
	}
	public int getPitch() {
		return pitch;
	}
	public void setPitch(int pitch) {
		this.pitch = pitch;
	}
	public int getGrey() {
		return grey;
	}
	public void setGrey(int grey) {
		this.grey = grey;
	}
	

}
